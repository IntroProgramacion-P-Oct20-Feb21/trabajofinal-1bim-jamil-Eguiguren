## Se pide que iingrese variabels para luego presentarlas en un orden es especifico
nombreAsignatura1 = input("Ingrese nombre de asignatura 1")
nombreAsignatura2 = input("Ingrese nombre de asignatura 2")
notaAsignatura1 = float(input("Ingrese nota de asignatura 1"))
notaAsignatura2 = float(input("Ingrese nota de asignatura 2"))
nombreestudiante = input("Ingrese nombre de estudiante")
print ("Asignatura 1: %s\nNota 1:%.2f\nAsignatura 2:%s\nNota 2:%.2f\n"\
	"Nombre del estudiante: %s\n"%(
		nombreAsignatura1,\
		notaAsignatura1,\
		nombreAsignatura2,\
		notaAsignatura2,\
		nombreestudiante))