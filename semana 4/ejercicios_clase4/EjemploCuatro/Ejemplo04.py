##Se realiza una suma con las variables ingreasadas para luego presentarlas
CPU = int(input("Ingrese el valor del CPU"))
teclado = int(input("Ingrese el valor del teclado"))
pantalla = int(input("Ingrese el valor de la patalla"))
raton = int(input("Ingrese el valor del raton"))
suma = CPU + teclado + pantalla + raton
print ("el costo de la computadora es: %i\n" % (suma))